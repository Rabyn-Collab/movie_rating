import 'package:hive_flutter/hive_flutter.dart';
part 'genres.g.dart';

@HiveType(typeId: 1)
class Genres extends HiveObject{
  @HiveField(0)
  final int id;

  @HiveField(1)
  final String name;


  Genres({required this.id, required this.name});
  factory Genres.fromJson(Map<String, dynamic> json){
    return Genres(
    id: json['id'],
    name:  json['name']
  );
  }


}

